import java.util.*;
public class nto1 {
    public static void main(String[] args) {
        Scanner in = new Scanner(System.in);
        System.out.println("ENTER THE LIMIT");
        int n=in.nextInt();
        int i=n;
        System.out.println("HERE IS YOUR ANSWER:");
        while(i>0){
            System.out.println(i);
            i--;
        }
    }
}